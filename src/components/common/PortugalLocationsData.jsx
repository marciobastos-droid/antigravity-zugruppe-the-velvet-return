// Obter todos os distritos
export const getDistritos = () => Object.keys(PORTUGAL_LOCATIONS);

// Obter concelhos de um distrito
export const getConcelhos = (distrito) => {
  if (!distrito || !PORTUGAL_LOCATIONS[distrito]) return [];
  return Object.keys(PORTUGAL_LOCATIONS[distrito].concelhos).sort();
};

// Obter freguesias de um concelho
export const getFreguesias = (distrito, concelho) => {
  if (!distrito || !concelho) return [];
  const concelhoData = PORTUGAL_LOCATIONS[distrito]?.concelhos?.[concelho];
  return concelhoData?.freguesias || [];
};

// Obter zonas de um concelho
export const getZonas = (distrito, concelho) => {
  if (!distrito || !concelho) return [];
  const concelhoData = PORTUGAL_LOCATIONS[distrito]?.concelhos?.[concelho];
  return concelhoData?.zonas || [];
};

// Obter todas as cidades/concelhos (para filtros simples)
export const getAllConcelhos = () => {
  const concelhos = [];
  Object.values(PORTUGAL_LOCATIONS).forEach(distrito => {
    Object.keys(distrito.concelhos).forEach(concelho => {
      concelhos.push(concelho);
    });
  });
  return [...new Set(concelhos)].sort();
};

// Obter todas as zonas (para filtros simples)
export const getAllZonas = () => {
  const zonas = [];
  Object.values(PORTUGAL_LOCATIONS).forEach(distrito => {
    Object.values(distrito.concelhos).forEach(concelho => {
      zonas.push(...(concelho.zonas || []));
    });
  });
  return [...new Set(zonas)].sort();
};

// Pesquisar localização por texto
export const searchLocations = (searchText) => {
  if (!searchText || searchText.length < 2) return [];
  
  const results = [];
  const searchLower = searchText.toLowerCase();
  
  Object.entries(PORTUGAL_LOCATIONS).forEach(([distrito, distritoData]) => {
    // Pesquisar no distrito
    if (distrito.toLowerCase().includes(searchLower)) {
      results.push({ type: 'distrito', name: distrito, path: distrito });
    }
    
    Object.entries(distritoData.concelhos).forEach(([concelho, concelhoData]) => {
      // Pesquisar no concelho
      if (concelho.toLowerCase().includes(searchLower)) {
        results.push({ 
          type: 'concelho', 
          name: concelho, 
          path: `${distrito} > ${concelho}`,
          distrito 
        });
      }
      
      // Pesquisar nas freguesias
      concelhoData.freguesias?.forEach(freguesia => {
        if (freguesia.toLowerCase().includes(searchLower)) {
          results.push({ 
            type: 'freguesia', 
            name: freguesia, 
            path: `${distrito} > ${concelho} > ${freguesia}`,
            distrito,
            concelho
          });
        }
      });
      
      // Pesquisar nas zonas
      concelhoData.zonas?.forEach(zona => {
        if (zona.toLowerCase().includes(searchLower)) {
          results.push({ 
            type: 'zona', 
            name: zona, 
            path: `${distrito} > ${concelho} > ${zona}`,
            distrito,
            concelho
          });
        }
      });
    });
  });
  
  return results.slice(0, 20); // Limitar resultados
};

// Obter distrito de um concelho
export const getDistritoByConcelho = (concelhoName) => {
  for (const [distrito, data] of Object.entries(PORTUGAL_LOCATIONS)) {
    if (data.concelhos[concelhoName]) {
      return distrito;
    }
  }
  return null;
};

